#!/usr/bin/env python3
"""
Generate benchmark_prompts.json from seed_prompts.json.

Seeds are included as-is. For travel_dining, credit_card, and structured_json,
light variations are generated by appending context overrides to the user message.

Usage: python prompts/prompt_generator.py [--variations-per-seed 2]
"""

import json
import copy
import argparse
from pathlib import Path

VARIATIONS = {
    "travel_dining": [
        {"city": "Boston", "budget": "$220/night", "cuisine": "modern American", "dietary": "vegetarian"},
        {"city": "Austin", "budget": "$180/night", "cuisine": "BBQ and Tex-Mex", "dietary": "none"},
        {"city": "Miami", "budget": "$240/night", "cuisine": "Latin American and seafood", "dietary": "pescatarian"},
    ],
    "credit_card": [
        {"spend": "flights $10k, hotels $5k, dining $3k", "priority": "points flexibility", "fee": "up to $550"},
        {"spend": "flights $5k, hotels $3k, dining $6k", "priority": "dining rewards", "fee": "under $100"},
        {"spend": "flights $20k, hotels $15k, dining $8k", "priority": "premium travel benefits", "fee": "any"},
    ],
    "structured_json": [
        {"city": "Nashville", "budget": "$80/person", "occasion": "client dinner"},
        {"city": "Seattle", "budget": "$110/person", "occasion": "birthday celebration"},
        {"city": "New Orleans", "budget": "$95/person", "occasion": "romantic dinner"},
    ],
}

CATEGORY_PREFIXES = {
    "travel_dining": "td", "credit_card": "cc", "merchant_offers": "mo",
    "multi_turn": "mt", "structured_json": "sj",
}


def _make_prompt(seed, prompt_id):
    p = {
        "id": prompt_id,
        "category": _category(seed["id"]),
        "type": "multi_turn" if "messages" in seed else "single_turn",
        "system": seed["system"],
        "quality_checks": seed["quality_checks"],
        "meta": copy.deepcopy(seed.get("meta", {})),
    }
    if "messages" in seed:
        p["messages"] = seed["messages"]
    else:
        p["user"] = seed["user"]
    p["meta"].setdefault("seed_id", seed["id"])
    return p


def _category(seed_id):
    for prefix, cat in CATEGORY_PREFIXES.items():
        if seed_id.startswith(cat + "_"):
            return prefix
    return "unknown"


def _vary(user_text, vals):
    override = ", ".join(f"{k}: {v}" for k, v in vals.items())
    return f"{user_text}\n\n[Context override: {override}]"


def generate(seeds_path, output_path, variations_per_seed=2):
    with open(seeds_path) as f:
        data = json.load(f)

    prompts = []
    for cat_name, seeds in data["categories"].items():
        var_table = VARIATIONS.get(cat_name, [])

        for seed in seeds:
            prompts.append(_make_prompt(seed, seed["id"]))

            for vi in range(min(variations_per_seed, len(var_table))):
                variant = copy.deepcopy(seed)
                variant["user"] = _vary(seed["user"], var_table[vi])
                p = _make_prompt(variant, f"{seed['id']}_v{vi+1}")
                p["meta"]["is_variation"] = True
                p["meta"]["seed_id"] = seed["id"]
                prompts.append(p)

    # Assign sequential IDs per category
    counters = {}
    for p in prompts:
        cat = p["category"]
        counters[cat] = counters.get(cat, 0) + 1
        prefix = CATEGORY_PREFIXES.get(cat, cat[:2])
        p["id"] = f"{prefix}_{counters[cat]:03d}"

    by_category = {}
    for p in prompts:
        by_category[p["category"]] = by_category.get(p["category"], 0) + 1

    output = {
        "version": "1.0",
        "generated_from": str(seeds_path),
        "total_prompts": len(prompts),
        "by_category": by_category,
        "prompts": prompts,
    }

    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(output, f, indent=2)

    print(f"Generated {len(prompts)} prompts -> {output_path}")
    for cat, count in by_category.items():
        print(f"  {cat}: {count}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--seeds", default="prompts/seed_prompts.json")
    parser.add_argument("--output", default="prompts/benchmark_prompts.json")
    parser.add_argument("--variations-per-seed", type=int, default=2)
    args = parser.parse_args()
    generate(args.seeds, args.output, args.variations_per_seed)
